async function main() {
  let inserted = 0;
  let errors = 0;

  for (const car of soldCars) {
    const record = {
      ...car,
      status: "prodano",
      is_published: true,
      sort_order: 99,
      description: null,
      defects: [],
      badges: ["Cebia"],
      youtube_url: null,
      equipment: {},
      meta_title: null,
      meta_description: null,
    };

    const { data, error } = await supabase
      .from("cars")
      .insert(record)
      .select("id, name")
      .single();

    if (error) {
      console.error(`CHYBA: ${car.name} — ${error.message}`);
      errors++;
    } else {
      console.log(`OK: ${data.name} (${data.id})`);
      inserted++;
    }
  }

  console.log(`\nHotovo: ${inserted} vloženo, ${errors} chyb`);
}

main();
